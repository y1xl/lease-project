<template>
  <div class="page bgc">
    <div class="pd-15">
      <div class="header">
        <div class="text-c">我的额度</div>
        <div class="text-c">{{users_money}}</div>
      </div>
    </div>

    <div class="pd-15 border-b rz-style">认证免押金方式</div>
    <router-link class="flex-jc-between pd-15 border-b" :to="realname==0?'/realname':'/realnameSuc'">
      <div>实名认证</div>
      <div class="flex-align-items">
        <span class="fc-blue mr" v-if="realname==0">去认证</span>
        <span class="fc-grey mr" v-else>已认证</span>
        <van-icon name="arrow"/>
      </div>
    </router-link>
    <div class="flex-jc-between pd-15 border-b" @click="getgo" v-if="school==1">
      <div>学籍认证</div>
      <div class="flex-align-items">
        <span class="fc-grey mr">已认证</span>
        <van-icon name="arrow"/>
      </div>
    </div>
    <div class="flex-jc-between pd-15 border-b" @click="getgo" v-else>
      <div>学籍认证</div>
      <div class="flex-align-items">
        <span class="fc-blue mr" >去认证</span>
        <van-icon name="arrow"/>
      </div>
    </div>
    <div class="flex-jc-between pd-15 border-b" @click="call">
      <div>邀请好友</div>
      <div class="flex-align-items">
        <van-icon name="arrow"/>
      </div>
    </div>
    <router-link class="flex-jc-between pd-15 border-b" to="/cpeople">
      <div>添加紧急联系人</div>
      <div class="flex-align-items">
        <van-icon name="arrow"/>
      </div>
    </router-link>
  </div>
</template>

<script>
import { Toast } from 'vant';
const nativeshare = () => import ('nativeshare') 
const m_share = () => import ('m-share')
var NativeShare, mShare

export default {
  data() {
    return {
      users_money: "",
      realname:'',
      school:''
    };
  },
  created() {
    this.userprice();
  },
  mounted() {
    nativeshare().then(res =>  {NativeShare = res.default} )
    m_share().then(res => {mShare = res})
  },
  methods: {
    userprice() {
      let userinfo = JSON.parse(window.localStorage.getItem("userinfo"));
      if (userinfo) {
        Toast.loading({ mask: true,message: '加载中...'})
        let postData = this.$qs.stringify({
          users_id: userinfo.users_id
        });
        this.axios
          .post(this.API + "api/Lease/user_price", postData)
          .then(res => {
            console.log(res.data, "user_price");
            let resdata = res.data;
            if (resdata.code == 200) {
              Toast.clear()
              this.users_money = resdata.data.users_money;
              this.realname = resdata.data.is_idcard
              this.school = resdata.data.is_chsi
            } else {
              Toast.clear()
              Toast(resdata.message);
            }
          });
      }
    },

    getgo() {
      Toast.loading({ mask: true, message: "加载中..." });
        let postData = this.$qs.stringify({
            users_id: JSON.parse(window.localStorage.getItem("userinfo")).users_id,
        });
        this.axios.post(this.API + "api/Order/GetCHISUrl", postData)
        .then(res => {
            console.log(res.data, "goschool");
            let resdata = res.data;
            if (resdata.code == 200) {
                window.location.href = resdata.data
            } else {
                Toast(resdata.message);
            }
        })
        .catch(error => {
            Toast('网络出错')
        });
    },

    call(){
      let config = {
        title: '数码租赁',
        link: window.location.origin + '#/login'
      }
      let shareData = {  //nativeShare的参数模型
          title: config.title,
          desc: '',
          // 如果是微信该link的域名必须要在微信后台配置的安全域名之内的。
          link: config.link,
          icon: '',
      }
      let mShareData = {  //m-share的参数模型
            title: config.title, // 标题，默认读取document.title
            desc: '', // 描述, 默认读取head标签：<meta name="description" content="desc" />
            link: config.link, // 网址，默认使用window.location.href
            imgUrl: '', // 图片, 默认取网页中第一个img标签
      }
      let nativeShare = new NativeShare()
      nativeShare.setShareData(shareData)
      try {
        nativeShare.call('wechatFriend')
      } catch(e) {
        mShare.to('wx', mShareData)
      }
    }
  }
};
</script>

